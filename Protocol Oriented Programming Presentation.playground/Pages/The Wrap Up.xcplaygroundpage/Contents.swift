## The Wrap Up

* When should you use Protocols?
    * Whenever there is a need for multiple classes to share a "Trait"
    * Apple: Always start with a Protocol


* Why should you use Protocols?
    * Protocols/Value types/Generics are highly optimized
    * Common code in one place shared by many classes of different types or even structs
    * With inheritance the only structure you can make is a hierarchy. But by placing your reusable code in protocol extensions, you’re no longer limited to just hierarchies — you’ve got more freedom in how you stick your building blocks together to build bigger components


